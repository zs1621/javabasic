public class SwitchStudy {
    public static void main(String[] strings) {
        int num = 1;
        switch(num) {
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            default:
                System.out.println("3---");
        }
    }
}
